pub fn hex(color: u28) u32 {
    const b: u32 = color & 0x0000ff;
    const g: u32 = (color & 0x00ff00) >> 8;
    const r: u32 = (color & 0xff0000) >> 16;
    return @as(u32, b) << 16 | @as(u32, g) << 8 | @as(u32, r) << 0;
}

